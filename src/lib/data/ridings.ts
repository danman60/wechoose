/**
 * Look up a single riding by its slug.
 */
export function getRidingBySlug(slug: string): Riding | undefined {
  return RIDINGS.find((r) => r.slug === slug);
}

/**
 * Given an FSA (first 3 characters of a Canadian postal code),
 * return matching riding slugs. Falls back to province-level
 * ridings based on the first letter if no specific mapping exists.
 */
export function getRidingsByFSA(fsa: string): Riding[] {
  const normalized = fsa.toUpperCase().replace(/\s/g, "");
  if (normalized.length < 3) return [];

  const key = normalized.slice(0, 3);

  // Direct lookup
  const slugs = FSA_TO_RIDINGS[key];
  if (slugs) {
    return slugs
      .map((s) => getRidingBySlug(s))
      .filter((r): r is Riding => r !== undefined);
  }

  // Province-level fallback by first letter
  const letter = key[0];
  const provinceMap: Record<string, string> = {
    A: "NL",
    B: "NS",
    C: "PE",
    E: "NB",
    G: "QC",
    H: "QC",
    J: "QC",
    K: "ON",
    L: "ON",
    M: "ON",
    N: "ON",
    P: "ON",
    R: "MB",
    S: "SK",
    T: "AB",
    V: "BC",
    X: "NT", // Could be NT or NU — return both territory ridings
    Y: "YT",
  };

  const province = provinceMap[letter];
  if (!province) return [];

  if (letter === "X") {
    // Territories — return both NT and NU
    return RIDINGS.filter((r) => r.province === "NT" || r.province === "NU");
  }

  return RIDINGS.filter((r) => r.province === province);
}

/**
 * Return all ridings in a given province/territory.
 * Accepts standard 2-letter codes: BC, AB, SK, MB, ON, QC, NB, NS, PE, NL, YT, NT, NU
 */
export function getRidingsByProvince(province: string): Riding[] {
  const normalized = province.toUpperCase();
  return RIDINGS.filter((r) => r.province === normalized);
}
